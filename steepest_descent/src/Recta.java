
public class Recta {
	private double m;
	private double b;
	
	
	public Recta(double m, double b) {
		super();
		this.m = m;
		this.b = b;
	}
	
	public Recta() {
		super();
		this.m = 0;
		this.b = 0;
	}
	
	
	public double getM() {
		return m;
	}
	public void setM(double m) {
		this.m = m;
	}
	public double getB() {
		return b;
	}
	public void setB(double b) {
		this.b = b;
	}
	
	
	

}
